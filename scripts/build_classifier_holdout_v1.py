def build_rows() -> list[dict[str, str]]:
    rows: list[dict[str, str]] = []
    rows.extend(row(text, 1, "en", "direct") for text in DIRECT_EN)
    rows.extend(row(text, 1, "en", "paraphrase") for text in PARAPHRASE_EN)
    rows.extend(row(text, 1, "ja", "direct") for text in DIRECT_JA)
    rows.extend(row(text, 1, "ja", "paraphrase") for text in PARAPHRASE_JA)
    rows.extend(row(text, 0, "en", "safe_normal") for text in SAFE_NORMAL_EN)
    rows.extend(row(text, 0, "en", "safe_fp") for text in SAFE_FP_EN)
    rows.extend(row(text, 0, "ja", "safe_normal") for text in SAFE_NORMAL_JA)
    rows.extend(row(text, 0, "ja", "safe_fp") for text in SAFE_FP_JA)
    return rows


def main() -> None:
    rows = build_rows()
    OUT.parent.mkdir(parents=True, exist_ok=True)
    with OUT.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=["text", "label", "source", "language", "category"],
        )
        writer.writeheader()
        writer.writerows(rows)
    print(f"wrote {OUT.relative_to(ROOT)} rows={len(rows)}")


if __name__ == "__main__":
    main()
